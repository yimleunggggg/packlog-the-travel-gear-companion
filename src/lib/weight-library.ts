//   2. Brand only (e.g. "Salomon" trail shoe → ~280 g per shoe = 320 g pair generic)
//   3. Generic noun (e.g. "backpack 55L" → ~1800 g)
//   4. Coarse category fallback (e.g. anything mentioning "shoes" → 700 g)
//
// All matches return both the weight estimate AND a normalized English name so
// "萨洛蒙ultra 4" → "Salomon Ultra 4" (490 g pair).
//
// Library is meant to grow. Easy to extend: append entries below.


/**
 * Suggest a weight + category + canonical names from a free-text user input.
 * Returns the highest-confidence match (longer specific tokens win).
 */
export function suggestFromName(name: string): WeightHint | null {
  const q = name.trim().toLowerCase();
  if (!q) return null;
  let best: WeightHint | null = null;
  let bestScore = -1;
  for (const h of allEntries) {
    for (const tok of h.tokens) {
      const t = tok.toLowerCase();
      if (q.includes(t)) {
        // score = base confidence + token length bonus
        const score = (h.weight ?? 30) + t.length;
        if (score > bestScore) {
          best = h;
          bestScore = score;
        }
      }
    }
  }
  return best;
}


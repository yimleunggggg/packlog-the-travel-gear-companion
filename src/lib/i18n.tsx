const Ctx = createContext<{
  lang: Lang;
  setLang: (l: Lang) => void;
  t: (k: string) => string;
}>({
  lang: "en",
  setLang: () => {},
  t: (k) => k,
});

export function I18nProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Lang>("en");

  const setLang = useCallback((l: Lang) => {
    if (l === "ja") return;
    setLangState(l);
  }, []);

  useEffect(() => {
    if (typeof window === "undefined") return;
    const saved = localStorage.getItem("packlog.lang") as Lang | null;
    if (!saved || !dicts[saved]) return;
    if (saved === "ja") {
      setLangState("en");
      localStorage.setItem("packlog.lang", "en");
      return;
    }
    setLangState(saved);
  }, []);

  useEffect(() => {
    if (typeof window !== "undefined") localStorage.setItem("packlog.lang", lang);
  }, [lang]);

  const t = (k: string) => dicts[lang][k] ?? dicts.en[k] ?? k;
  return <Ctx.Provider value={{ lang, setLang, t }}>{children}</Ctx.Provider>;
}

export const useI18n = () => useContext(Ctx);

// Helper: pick the right localized name for an item/gear
export function pickName(
  lang: Lang,
  obj: { name?: string; nameEn?: string; nameZh?: string },
): string {
  if (lang === "zh") return obj.nameZh ?? obj.nameEn ?? obj.name ?? "";
  if (lang === "ja") return obj.nameEn ?? obj.name ?? obj.nameZh ?? "";
  return obj.nameEn ?? obj.name ?? obj.nameZh ?? "";
}

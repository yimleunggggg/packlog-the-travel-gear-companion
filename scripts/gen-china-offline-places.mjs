/**
 * Reads src/lib/data/china-pc-code.json (from modood/Administrative-divisions-of-China, MIT)
 * and writes src/lib/china-offline-places.generated.ts for fully offline destination search.
 *
 * Drops rows whose pinyin key matches an existing curated city in destinations.ts
 * (so 沈阳 stays the hand-picked id, while still adding missing prefectures).
 *
 * Run: npm run gen:china-offline
 */
import { readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";
import { pinyin } from "pinyin-pro";

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, "..");

const raw = JSON.parse(readFileSync(join(root, "src/lib/data/china-pc-code.json"), "utf8"));
const destTs = readFileSync(join(root, "src/lib/destinations.ts"), "utf8");

/** Pinyin token for search / dedupe (no tones, concatenated). */
function pinyinKey(zh) {
  const bare = zh
    .replace(/市$/u, "")
    .replace(/区$/u, "")
    .replace(/县$/u, "")
    .replace(/州$/u, "")
    .replace(/盟$/u, "")
    .replace(/地区$/u, "")
    .replace(/林区$/u, "");
  return pinyin(bare, { toneType: "none", type: "array" }).join("").toLowerCase();
}

function titleCaseJoined(zh) {
  const bare = zh
    .replace(/市$/u, "")
    .replace(/区$/u, "")
    .replace(/县$/u, "")
    .replace(/州$/u, "")
    .replace(/盟$/u, "")
    .replace(/地区$/u, "")
    .replace(/林区$/u, "");
  const arr = pinyin(bare, { toneType: "none", type: "array" });
  const joined = arr.join("");
  return joined.charAt(0).toUpperCase() + joined.slice(1);
}

const curatedKeys = new Set();
const curatedRe = /c\("cn-[^"]+",\s*"([^"]+)",\s*"([^"]+)"\)/g;
let cm;
while ((cm = curatedRe.exec(destTs))) {
  const en = cm[1];
  const zh = cm[2];
  curatedKeys.add(pinyinKey(zh));
  curatedKeys.add(en.toLowerCase().replace(/\s+/g, ""));
}

const rows = [];
for (const prov of raw) {
  const provName = prov.name;
  const children = prov.children ?? [];
  if (children.length === 0) continue;
  const allDistricts = children.every((ch) => String(ch.code).length === 6);
  if (allDistricts) {
    rows.push({
      code: `${String(prov.code)}0000`.slice(0, 6),
      zh: provName,
      provZh: provName,
      en: titleCaseJoined(provName),
      key: pinyinKey(provName),
    });
  } else {
    for (const ch of children) {
      const code = String(ch.code);
      if (code.length > 6) continue;
      rows.push({
        code,
        zh: ch.name,
        provZh: provName,
        en: titleCaseJoined(ch.name),
        key: pinyinKey(ch.name),
      });
    }
  }
}

const filtered = rows.filter((r) => {
  if (curatedKeys.has(r.key)) return false;
  if (curatedKeys.has(r.en.toLowerCase())) return false;
  return true;
});

const ts = `/* eslint-disable */
// Generated by scripts/gen-china-offline-places.mjs — do not edit by hand.
// Source: src/lib/data/china-pc-code.json (modood/Administrative-divisions-of-China, MIT)
// Regenerate: npm run gen:china-offline

export type OfflineChinaPlaceRow = {
  /** Administrative code (4–6 digits) — stable id fragment */
  code: string;
  zh: string;
  provZh: string;
  /** Latin label for English UI / search (pinyin, no tones) */
  en: string;
  /** Lowercase pinyin concatenation for search / dedupe */
  key: string;
};

export const OFFLINE_CHINA_PLACE_ROWS: readonly OfflineChinaPlaceRow[] = ${JSON.stringify(filtered, null, 2)} as const;
`;

writeFileSync(join(root, "src/lib/china-offline-places.generated.ts"), ts, "utf8");
console.log(
  `Wrote ${filtered.length} offline China rows (${rows.length - filtered.length} skipped vs curated tree).`,
);
